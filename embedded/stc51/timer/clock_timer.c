#include <mcs51/8051.h>

#include "lcd1602.h"

/**
 * 函数名: timer0Init
 * 功能: 初始化8051单片机的定时器/计数器0，配置为16位定时器模式，
 *       定时周期约1ms（晶振频率为11.0592MHz）
 */
void timer0Init() {

    /*-------- 第一步：配置定时器0的工作模式 --------*/

    // TMOD 寄存器（8位）的高4位控制定时器1，低4位控制定时器0
    // 低4位结构: [GATE | C/T | M1 | M0]
    //   GATE = 0: 由TR0单独控制定时器启停（不需要INT0引脚配合）
    //   C/T   = 0: 选择定时器模式（对内部时钟计数），而非计数器模式
    //   M1:M0 = 01: 选择工作模式1——16位定时器（TH0+TL0共16位）
    //
    // 第一步：将TMOD的低4位清零，高4位保持不变（不影响定时器1的配置）
    //         TMOD & 0xF0 => 低4位全部清零，高4位原样保留
    TMOD = TMOD & 0xF0;   // 0xF0 = 1111 0000，屏蔽低4位

    // 第二步：将低4位设置为 0001（即模式1）
    //         0x01 = 0000 0001，即 GATE=0, C/T=0, M1=0, M0=1
    TMOD = TMOD | 0x01;   // 与上面清零后的值做或运算，置位最低位

    /*-------- 第二步：清除溢出标志位 --------*/

    // TF0 是定时器0的溢出标志位
    // 当定时器从0xFFFF溢出回0x0000时，硬件自动将TF0置1
    // 初始化时手动清零，避免残留标志触发误中断
    TF0 = 0;

    /*-------- 第三步：启动定时器0 --------*/

    // TR0 是定时器0的运行控制位
    // TR0 = 1: 启动定时器0，开始从TH0:TL0向上计数
    // TR0 = 0: 停止定时器0
    TR0 = 1;

    /*-------- 第四步：装载定时初值 --------*/

    // 目标：定时约1ms（假设晶振 fosc = 11.0592）
    //
    // 8051的机器周期 11.0592Mhz / 12 = 921600 Hz
    // 16位定时器最大计数值 = 2^16 = 65536
    // 定时1ms需要计数次数 = 921600Hz / 1000 ~= 921.6
    // 初值 = 65536 - 921.6 ~= 64615
    //
    // 将16位初值 64615 拆分为高8位和低8位：
    //   TH0 = 初值 / 256   （取高8位，即右移8位）
    //   TL0 = 初值 % 256   （取低8位，即取余数）
    //
    TH0 = 64615 / 256;    // 装载高8位初值
    TL0 = 64615 % 256;    // 装载低8位初值

    /*-------- 第五步：使能中断 --------*/

    // ET0 是定时器0的中断允许位（位于IE寄存器中）
    // ET0 = 1: 允许定时器0溢出时向CPU请求中断
    ET0 = 1;

    // EA 是总中断允许位（Interrupt Enable，位于IE寄存器最高位）
    // EA = 1: 打开全局中断开关（所有被允许的中断源都可以响应）
    // EA = 0: 关闭全局中断，任何外设中断都不会被响应
    // 注意：ET0和EA必须同时置1，定时器0中断才能真正被响应
    EA = 1;

    /*-------- 第六步：设置中断优先级 --------*/

    // PT0 是定时器0的中断优先级控制位（位于IP寄存器中）
    // PT0 = 0: 定时器0中断设为低优先级
    // PT0 = 1: 定时器0中断设为高优先级
    // 多个中断同时发生时，高优先级的中断会优先被响应
    PT0 = 0;  // 设为低优先级（默认值，此处显式设置确保明确）
}

void timer0Init() {
    // 配置工作模式
    TMOD = TMOD & 0xF0;  // 0000  0001
    TMOD = TMOD | 0x01;
    TF0 = 0;
    TR0 = 1;
    TH0 = 64615 / 256;
    TL0 = 64615 % 256;
    ET0 = 1;
    EA = 1;
    PT0 = 0;
}

static unsigned int g_count = 0;
static unsigned int g_hour = 0;
static unsigned int g_min = 0;
static unsigned int g_sec = 0;

void timer0Routine() __interrupt(1) {
    // 中断触发，重新设置定时器初始值
    TH0 = 64615 / 256;
    TL0 = 64615 % 256;
    g_count++;
    if (g_count > 1000) {
        g_count = 0;
        g_sec++;
        if (g_sec == 60) {
            g_min++;
            g_sec = 0;
            if (g_min == 60) {
                g_hour++;
                g_min = 0;
            }
        }
    }
}

void main() {

    LCD_init();
    timer0Init();

    LCD_showString(1, 0, "Clock:");
    LCD_showString(2, 0, "  :  :");

    while (1) {
        LCD_showNumber(2, 1, g_hour, 10);
        LCD_showNumber(2, 4, g_min, 10);
        LCD_showNumber(2, 7, g_sec, 10);
    }
    
}

